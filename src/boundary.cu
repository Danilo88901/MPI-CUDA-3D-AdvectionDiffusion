__global__ void boundary_conditions_x(int height, int width, int length, double* phi) {
	int i = blockDim.z * blockIdx.z + threadIdx.z;
	int j = blockDim.y * blockIdx.y + threadIdx.y;
	if (i >= height || j >= width)return;
	int nx = length;
	int ny = width;
	int nz = height;

	// ==========================================
	// X direction (k-??????, ???????? ??????? ????)
	// ==========================================
	int base_x = i * ny * nx + j * nx;

	// ????? ????????? ?????? (0 ? 1) ???????? ?????? ? ??????? ????? ?????????? ???????
	phi[base_x + 0] = phi[base_x + (nx - 4)]; // ????? ???? ????? ?????? ????????
	phi[base_x + 1] = phi[base_x + (nx - 3)]; // ????? ??????? ??????????? ????

	// ?????? ????????? ?????? (nx-2 ? nx-1) ???????? ?????? ? ?????? ?????
	phi[base_x + (nx - 2)] = phi[base_x + 2]; // ????? ?????? ??????????? ????
	phi[base_x + (nx - 1)] = phi[base_x + 3]; // ????? ???? ????? ????? ???????		
}
__global__ void  boundary_conditions_y(int height, int width, int length, double* phi) {
	int i = blockDim.z * blockIdx.z + threadIdx.z;
	int k = blockDim.x * blockIdx.x + threadIdx.x;
	if (i >= height || k >= length)return;

	int nx = length;
	int ny = width;
	int nz = height;
	int base_idx = i * ny * nx + k;

	// ghost_front (j=0, j=1) <- ???????? ? ???????? ????? ?? Y
	phi[base_idx + 0 * nx] = phi[base_idx + (ny - 4) * nx];
	phi[base_idx + 1 * nx] = phi[base_idx + (ny - 3) * nx];

	// ghost_back (j=ny-2, j=ny-1) <- ???????? ? ???????? ????? ?? Y
	phi[base_idx + (ny - 2) * nx] = phi[base_idx + 2 * nx];
	phi[base_idx + (ny - 1) * nx] = phi[base_idx + 3 * nx];
}
__global__ void boundary_conditions_z(int height, int width, int length, double* phi) {
	int j = blockDim.y * blockIdx.y + threadIdx.y;
	int k = blockDim.x * blockIdx.x + threadIdx.x;
	if (j >= width || k >= length)return;

	int nx = length;
	int ny = width;
	int nz = height;
	int base_idx = j * nx + k;

	// ghost_bottom (i=0, i=1) <- ???????? ? ???????? ????? ?? Z
	phi[base_idx + 0 * ny * nx] = phi[base_idx + (nz - 4) * ny * nx];
	phi[base_idx + 1 * ny * nx] = phi[base_idx + (nz - 3) * ny * nx];

	// ghost_top (i=nz-2, i=nz-1) <- ???????? ? ??????? ????? ?? Z
	phi[base_idx + (nz - 2) * ny * nx] = phi[base_idx + 2 * ny * nx];
	phi[base_idx + (nz - 1) * ny * nx] = phi[base_idx + 3 * ny * nx];
}
